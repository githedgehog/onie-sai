#!/bin/bash
echo "S6100: Reset line cards.."
outb 0x210 0x04
outb 0x211 0xd9
outb 0x213 0x2
outb 0x211 0xda
outb 0x213 0x2
outb 0x211 0xdb
outb 0x213 0x2
outb 0x211 0xdc
outb 0x213 0x2
outb 0x211 0xdd
echo "S6100: power off done."
sleep 1
outb 0x210 0x04
outb 0x211 0xd9
outb 0x213 0x1
outb 0x211 0xda
outb 0x213 0x1
outb 0x211 0xdb
outb 0x213 0x1
outb 0x211 0xdc
outb 0x213 0x1
outb 0x211 0xdd
echo "S6100: power on done."
